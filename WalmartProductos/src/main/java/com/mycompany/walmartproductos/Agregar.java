    private void cargar(){
        String archivoJSON = "productos.json";
        listaProductos.clear();
        try {
            BufferedReader br = new BufferedReader(new FileReader(archivoJSON));
            StringBuilder resultado = new StringBuilder();
            String lectura;
            while ((lectura = br.readLine()) != null) {
                resultado.append(lectura);
            }
            br.close();

            String json = resultado.toString();

            Gson gson = new Gson();
            Producto[] productos = gson.fromJson(json, Producto[].class);

            for (Producto producto : productos) {
                listaProductos.add(producto);
            }

            for (Producto producto : listaProductos) {
                System.out.println("Nombre: " + producto.getNombre());
                System.out.println("Imagen: " + producto.getDireccion());
                System.out.println("Precio: " + producto.getPrecio());
                System.out.println("Descripci√≥n: " + producto.getDescripcion());
                System.out.println();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    private void guardar(){
        String nombre = jTextField1.getText();
        String imagen = jTextField4.getText();
        String precio = jTextField2.getText();
        String descripcion = jTextField3.getText();

        Producto nuevoProducto = new Producto();
        nuevoProducto.setNombre(nombre);
        nuevoProducto.setDireccion(imagen);
        nuevoProducto.setPrecio(precio);
        nuevoProducto.setDescripcion(descripcion);

        listaProductos.add(nuevoProducto);

        try (FileWriter fileWriter = new FileWriter("productos.json")) {
            Gson gson = new Gson();
            gson.toJson(listaProductos, fileWriter);
        } catch (IOException e) {
            e.printStackTrace();
        }

    }
    private void limpiar(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
    }
